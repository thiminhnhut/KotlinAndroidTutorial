class FunctionKotlin {
    fun tong_kotlin(a: Int, b: Int): Int {
        return a + b
    }

    fun hieu_kotlin(a: Int, b: Int): Int {
        return a - b
    }

    fun tich_kotlin(a: Int, b: Int): Int {
        return a*b;
    }
}